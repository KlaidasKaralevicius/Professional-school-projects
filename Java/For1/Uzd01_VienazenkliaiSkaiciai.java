public class Uzd01_VienazenkliaiSkaiciai {
	public static void main(String[] args) {
		
	int i = 0;
		
	while (i < 10) {
		System.out.println(i);
		i++;
	}
	
	System.out.println("---------------------------");
		
	for (int j = 0; j < 10; j++) {
		System.out.println(j);
		}
	}
}
