package For1;

public class Uzd02_Kvadratu {

	public static void main(String[] args) {
		
		int i = 0;
		
		while (i < 10) {
			System.out.println(i + " - " + i * i);
			i++;
		}
		
		System.out.println("----------------------");
		
		for (int j = 0; j < 10; j++) {
			System.out.println(j + " - " + j * j);
		}

	}

}
